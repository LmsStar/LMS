package day05;

public class ForDemo {
    public static void main(String[] args) {
        for(int i = 0;i<5;i++){
            System.out.println("行动是成功的阶梯");
        }
        /*
        * i = 0 true 输出
        * i = 1 true 输出
        * i = 2 true 输出
        * i = 3 true 输出
        * i = 4 true 输出
        * i = 5 false 循环结束
        * */
    }
}
